# 2. ANÁLISE DE ELASTICIDADE-PREÇO DA DEMANDA (E_d)
# =============================================================================
def analyze_price_elasticity(df):
    """
    Calcula a Elasticidade-Preço da Demanda (E_d) durante dias de promoção.
    E_d = (% variação na quantidade vendida) / (% variação no preço)
    """
    print(">> Calculando a Elasticidade-Preço da Demanda (Price Elasticity of Demand)...")

    baseline_sales = df[df["discount_pct"] == 0]["daily_sales"].mean()
    baseline_price = df["base_price"].iloc[0]

    discount_levels = [0.20, 0.33, 0.50, 0.75]
    elasticity_data = []

    for disc in discount_levels:
        sales_at_disc = df[df["discount_pct"] == disc]["daily_sales"].mean()
        p_eff = baseline_price * (1 - disc)

        pct_change_q = (sales_at_disc - baseline_sales) / baseline_sales
        pct_change_p = (p_eff - baseline_price) / baseline_price

        # Fórmula da Elasticidade
        e_d = pct_change_q / pct_change_p

        elasticity_data.append({
            "Desconto (%)": f"{int(disc*100)}%",
            "Preço Promocional ($)": f"{p_eff:.2f}",
            "Vendas Diárias Médias": int(sales_at_disc),
            "Variacão Qtd (%)": f"{pct_change_q * 100:+.1f}%",
            "Elasticidade (E_d)": round(e_d, 2)
        })

    elasticity_df = pd.DataFrame(elasticity_data)
    print("\n===== TABELA DE ELASTICIDADE-PREÇO DA DEMANDA =====")
    print(elasticity_df.to_string(index=False))

    # Gráfico de Elasticidade
    fig, ax = plt.subplots(figsize=(7, 4.5))
    discs = [d * 100 for d in discount_levels]
    e_vals = [abs(float(row["Elasticidade (E_d)"])) for row in elasticity_data]

    ax.plot(discs, e_vals, marker="o", color="#66C0F4", linewidth=2.5, markersize=8)
    ax.axhline(1.0, linestyle="--", color="#E85D75", label="E_d = 1.0 (Demanda Neutra)")
    ax.set_title("Elasticidade-Preço da Demanda por Faixa de Desconto", color="white")
    ax.set_xlabel("Percentual de Desconto na Steam (%)")
    ax.set_ylabel("Elasticidade |E_d| (Módulo)")
    ax.legend()
    plt.tight_layout()
    plt.savefig(f"{FIG_DIR}/01_elasticidade_preco.png")
    plt.close()


