# 1. GERADOR DE SÉRIE TEMPORAL SINTÉTICA DE JOGOS DA STEAM
# =============================================================================
def generate_steam_timeseries(days=730):
    """
    Gera uma série diária de 2 anos simulando métricas de um jogo na Steam.
    Inclui tendência, sazonalidade de fim de semana, promoções e ruído.
    """
    print(">> Gerando série temporal histórica de métricas da Steam...")
    np.random.seed(42)
    dates = pd.date_range(start="2024-01-01", periods=days, freq="D")

    # 1. Componentes da Série
    base_ccu = 15000
    decay_trend = np.exp(-np.linspace(0, 2.5, days)) * 30000  # Queda natural pós-lançamento
    steady_trend = 5000

    # Sazonalidade semanal (Picos no fim de semana)
    day_of_week = dates.dayofweek
    weekend_seasonality = np.where(day_of_week >= 5, 1.4, 1.0)

    # Promoções (Steam Summer Sale, Winter Sale, Discounts aleatórios)
    is_sale_event = np.zeros(days)
    # Summer sale (Julho) e Winter Sale (Dezembro)
    for i, date in enumerate(dates):
        if (date.month == 7 and 10 <= date.day <= 24) or (date.month == 12 and 18 <= date.day <= 31):
            is_sale_event[i] = 1
        elif np.random.rand() < 0.03: # Desconto pontual de distribuidora
            is_sale_event[i] = 1

    # Desconto aplicado (%)
    discount_pct = np.where(is_sale_event == 1, np.random.choice([0.20, 0.33, 0.50, 0.75], size=days), 0.0)
    base_price = 49.99
    effective_price = base_price * (1 - discount_pct)

    # Elasticidade da demanda: Descontos geram picos em CCU e vendas
    price_boost = 1.0 + (discount_pct * 2.8)

    # CCU Final (Peak Concurrent Users)
    noise = np.random.normal(1.0, 0.08, size=days)
    ccu = (base_ccu + decay_trend + steady_trend) * weekend_seasonality * price_boost * noise

    # Unidades vendidas estimadas (proporcional ao CCU)
    daily_sales = (ccu * np.random.uniform(0.05, 0.12, size=days) * price_boost).astype(int)

    df = pd.DataFrame({
        "date": dates,
        "ccu": ccu.astype(int),
        "daily_sales": daily_sales,
        "base_price": base_price,
        "discount_pct": discount_pct,
        "effective_price": effective_price,
        "is_sale_event": is_sale_event.astype(int),
        "day_of_week": day_of_week,
        "month": dates.month
    }).set_index("date")

    return df


