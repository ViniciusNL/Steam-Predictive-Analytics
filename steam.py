# 5. MODELAGEM PREDITIVA E FORECASTING DE CCU
# =============================================================================
def train_ccu_forecasting_model(df):
    """
    Treina um modelo de Machine Learning para prever o pico diário de jogadores (CCU).
    A divisão é temporal (Train nos primeiros 80% do tempo, Test nos últimos 20%).
    """
    print(">> Treinando modelo de Forecasting de Jogadores Simultâneos (Random Forest)...")

    df_feat = create_timeseries_features(df)

    features = [c for c in df_feat.columns if c not in ["ccu", "daily_sales"]]
    target = "ccu"

    # Split Temporal (Sem Shuffle)
    split_idx = int(len(df_feat) * 0.8)
    train = df_feat.iloc[:split_idx]
    test = df_feat.iloc[split_idx:]

    X_train, y_train = train[features], train[target]
    X_test, y_test = test[features], test[target]

    # Modelagem
    model = RandomForestRegressor(n_estimators=200, max_depth=10, random_state=42)
    model.fit(X_train, y_train)

    preds = model.predict(X_test)

    # Avaliação do Modelo
    rmse = np.sqrt(mean_squared_error(y_test, preds))
    mape = mean_absolute_percentage_error(y_test, preds) * 100
    r2 = r2_score(y_test, preds)

    print("\n===== DESEMPENHO DO MODELO DE FORECASTING (TESTE) =====")
    print(f"  RMSE (Erro Quadrático Médio): {rmse:.2f} jogadores")
    print(f"  MAPE (Erro Percentual Médio): {mape:.2f}%")
    print(f"  R² Score (Variância Explicada): {r2:.4f}")

    # Visualização: Real vs Predito
    fig, ax = plt.subplots(figsize=(10, 5))
    ax.plot(test.index, y_test.values, label="CCU Real", color="#66C0F4", linewidth=1.8)
    ax.plot(test.index, preds, label="CCU Predito (ML)", color="#FFB74D", linestyle="--", linewidth=1.8)

    # Destaque para eventos de promoção no conjunto de teste
    sales_in_test = test[test["is_sale_event"] == 1]
    if not sales_in_test.empty:
        ax.scatter(sales_in_test.index, sales_in_test["ccu"], color="#E85D75",
                   label="Steam Sale Event", zorder=5, s=35)

    ax.set_title("Previsão de Jogadores Simultâneos (CCU Forecast)", color="white")
