# 4. FEATURE ENGINEERING PARA SÉRIES TEMPORAIS (LAGS & ROLLING)
# =============================================================================
def create_timeseries_features(df):
    """
    Cria variáveis defasadas (Lags), médias móveis e atributos calendários.
    """
    print(">> Criando Lags, Médias Móveis e Atributos Calendários para Machine Learning...")
    df_feat = df.copy()

    # Lag Features (Valores passados)
    for lag in [1, 2, 7, 14]:
        df_feat[f"ccu_lag_{lag}"] = df_feat["ccu"].shift(lag)

    # Rolling Features (Médias e Desvios móveis)
    df_feat["ccu_rolling_7_mean"] = df_feat["ccu"].shift(1).rolling(window=7).mean()
    df_feat["ccu_rolling_30_mean"] = df_feat["ccu"].shift(1).rolling(window=30).mean()
    df_feat["ccu_rolling_7_std"] = df_feat["ccu"].shift(1).rolling(window=7).std()

    # Atributos Cíclicos do Calendário
    df_feat["is_weekend"] = np.where(df_feat["day_of_week"] >= 5, 1, 0)

    df_feat = df_feat.dropna()
    return df_feat


