def clean_and_transform(df):
    print(">> Tratando dados e criando novas features de jogos...")

    # Tratando nulos se existirem
    df = df.dropna().copy()

    # Novas Features
    df["is_free"] = (df["price"] == 0).astype(int)
    df["log_reviews"] = np.log1p(df["total_reviews"]) # Suavização logarítmica para reviews
    df["estimated_revenue"] = df["price"] * df["total_reviews"] # Sucesso Comercial estimado
    df["commercial_success"] = (df["estimated_revenue"] > df["estimated_revenue"].median()).astype(int)

    return df


