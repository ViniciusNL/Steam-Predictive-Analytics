# 1. GERADOR / CARREGADOR DE DADOS DA STEAM (SINTÉTICO PARA EXECUÇÃO)
# =============================================================================
def load_steam_data(n_samples=5000):
    print(">> Sincronizando e Carregando catálogo de jogos da Steam...")
    np.random.seed(42)

    publishers = ["Valve", "Electronic Arts", "Ubisoft", "Devolver Digital",
                  "Indie Studio A", "Indie Studio B", "Square Enix", "Klei Entertainment"]
    genres_list = ["Action", "RPG", "Strategy", "Casual", "Indie", "Adventure", "Simulation"]

    data = {
        "game_id": range(1000, 1000 + n_samples),
        "price": np.random.choice([0, 9.99, 14.99, 19.99, 29.99, 59.99, 69.99], size=n_samples, p=[0.15, 0.2, 0.25, 0.15, 0.1, 0.1, 0.05]),
        "total_reviews": np.random.exponential(scale=1500, size=n_samples).astype(int) + 10,
        "is_indie": np.random.choice([1, 0], size=n_samples, p=[0.65, 0.35]),
        "publisher": np.random.choice(publishers, size=n_samples),
        "primary_genre": np.random.choice(genres_list, size=n_samples),
        "days_since_release": np.random.randint(30, 3650, size=n_samples)
    }

    df = pd.DataFrame(data)

    # Gerando regras de negócio realistas para avaliações
    # Jogos indies e jogos com preço razoável tendem a ter avaliações ligeiramente mais altas
    base_rating = 0.70 + (df["is_indie"] * 0.08) - (df["price"] / 500) + np.random.normal(0, 0.1, n_samples)
    df["positive_ratio"] = np.clip(base_rating, 0.1, 0.99)
    df["positive_reviews"] = (df["total_reviews"] * df["positive_ratio"]).astype(int)
    df["high_rating"] = (df["positive_ratio"] >= 0.80).astype(int) # Alvo de classificação

    print(f"   Dataset carregado: {df.shape[0]} jogos e {df.shape[1]} atributos.")
    return df


