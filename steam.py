def run_clusterization_pca(df):
    print(">> Executando Clusterização (K-Means) e Redução de Dimensionalidade (PCA)...")
    features_cluster = ["price", "log_reviews", "positive_ratio", "days_since_release"]

    X_clust = df[features_cluster]
    scaler = StandardScaler()
    X_scaled = scaler.fit_transform(X_clust)

    # K-Means
    kmeans = KMeans(n_clusters=3, random_state=42, n_init=10)
    df["cluster"] = kmeans.fit_predict(X_scaled)

    # PCA para projeção 2D
    pca = PCA(n_components=2)
    pca_coords = pca.fit_transform(X_scaled)
    df["pca_1"] = pca_coords[:, 0]
    df["pca_2"] = pca_coords[:, 1]

    fig, ax = plt.subplots(figsize=(8, 6))
    sns.scatterplot(data=df, x="pca_1", y="pca_2", hue="cluster", palette="viridis", alpha=0.7, ax=ax)
    ax.set_title("Agrupamento de Jogos na Steam (Clusterização K-Means + PCA 2D)", color="white")
